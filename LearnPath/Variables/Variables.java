package LearnPath.Variables;

public class Variables {
    public static void main(String[] args) {
        int myAge; // Declare an integer variable called myAge
        myAge = 30; // Assign the value 30 to myAge

        double myHeight = 1.75; // Declare and initialize a double variable called myHeight
        String myName = "John"; // Declare and initialize a String variable called myName

        System.out.println(
                "My name is " + myName + ", I am " + myAge + " years old, and I am " + myHeight + " meters tall.");

    }

}
